<h1 style="color:#CD5C5C">Приготовление</h1>

<p>1. Говядину промойте и нарежьте небольшими кусочками. Выберите жилки и пленочки. </p>
<p>2. Луковицу и чеснок очистите. Лук нарежьте полукольцами, чеснок измельчите. </p>
<p>3. Болгарский перец промойте, вырежьте семечки и плодоножки, нарежьте крупными полукольцами.<p> 
<p>4. Помидоры промойте, вырежьте плодоножки. Если не ленитесь, снимите шкурку, опустив помидор на пару минут в кипяток. Нарежьте крупными кубиками.</p> 
<p>5. Картошку промойте, очистите, нарежьте крупными кубиками. Отлично в гуляше смотрится маленькая свежая картошечка. Ее даже можно не чистить и не резать, просто хорошенько промыть. </p>
<p>6. В большой кастрюле с толстыми стенками нагрейте растительное масло или растопите жир.</p>
<p>7. Выложите в сковороду лук и обжарьте, помешивая, до золотистого цвета. </p>
<p>8. Уменьшите огонь и всыпьте молотую паприку. Постоянно помешивайте. </p>
<p>9. Добавьте мясо и перемешайте так, чтобы кусочки мяса были в паприке.</p> 
<p>10. Обжарьте на медленном огне. Мясо должно приобрести нежно-коричневый оттенок. </p>
<p>11. Затем добавьте лавровый лист, тмин, соль, перец и измельченный чеснок. </p>
<p>12. Добавьте воды - столько, чтобы она покрыла ингредиенты. Оставьте тушиться на средне-медленном огне на 30-40 минут.</p> 
<p>13. По истечению указанного времени добавьте болгарский перец, помидоры и картошку. Добавьте воды, если нужно (столько, чтобы все было ею покрыто) и готовьте еще полчаса. Готовое мясо и овощи должны быть мягкими. </p>
<p>Гуляш с болгарским перцем готов! </p>
<p>Подавать гуляш желательно со сметаной, рубленой петрушкой и белым хлебом. Хлеб можно макать в соус и скушать это чудесное блюдо до последней капли.</p> 
